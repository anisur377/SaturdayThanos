package com.upskill.java_2;

public class SwitchHome {
	static int day = 3;

	public static void main(String[] args) {
		day();

	}
	public static void day(){
		switch (day){
		case 1:
		System.out.println("MONDAY");
		break;
		case 2:
		System.out.println("TUESDAY");
		break;
		case 3:
		System.out.println("WEDNESDAY");
		break;
		}
		
	}

}
